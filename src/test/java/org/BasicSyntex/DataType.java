package org.BasicSyntex;

public class DataType {

    public static void main(String[]args) {

        int       a = 5;
        float     b = 1.44f;
        double    c = 5.666666778;
        char      d = 'A';
        byte      e = 127;
        boolean   f = true;
        String    g = "Abdul Alim";

        System.out.println("Integer value: "   +a);
        System.out.println("Floating Value: "  +b);
        System.out.println("Double Value: "    +c);
        System.out.println("Character Value: " +d);
        System.out.println("Byte Value: "      +e);
        System.out.println("Boolean Value: "   +f);
        System.out.println("String Value: "    +g);

        System.out.println("Integer: "+a + "\nFloating:" +b + "\nDouble: "+c+"\nCharacter: " +d+ "\nCharacter: "+d+
                "\nByte: "+e+ "\nBoolean: "+f+"\nString: " +g);

    }
}
